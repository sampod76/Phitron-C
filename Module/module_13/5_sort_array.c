#include <stdio.h> // ইনপুট-আউটপুট ফাংশনের জন্য স্ট্যান্ডার্ড হেডার ফাইল
// https://phitron.io/ph028/video/ph028-13-9-selection-sort  // a
int main()
{
    int n; // 5
    scanf("%d", &n);

    int a[n]; // 9 3 1 7 5

    for (int i = 0; i < n; i++)
    {
        scanf("%d", &a[i]);
    }

    // ✅ দুটি লুপ ব্যবহার করে অ্যারের ভ্যালুগুলোকে ছোট থেকে বড় ক্রমে সাজানো হচ্ছে
    // এই অ্যালগরিদমটি একটি সাধারণ "Bubble sort" বা "Selection sort"-এর মতো কাজ করে
    //! মূল কথা হচ্ছে array প্রথম থেকে শুরু করে প্রত্যেকে সাথে প্রত্যেকের তুলনা করা হবে এবং যে ছোট তাকে আগে বসানো হবে
    for (int i = 0; i < n; i++) // বাইরের লুপ — একবারে একটি এলিমেন্টের অবস্থান ঠিক করবে
    {
        for (int j = i + 1; j < n; j++) // ভিতরের লুপ — পরের এলিমেন্টগুলোর সাথে তুলনা করবে
        {
            if (a[i] > a[j]) // যদি বর্তমান এলিমেন্ট পরেরটির চেয়ে বড় হয়
            {
                // তাহলে তাদের জায়গা অদলবদল (swap) করা হবে
                int temp = a[i]; // a[i]-এর ভ্যালু অস্থায়ীভাবে temp-এ রাখা হচ্ছে
                a[i] = a[j];     // a[i]-এ ছোট ভ্যালু বসানো হচ্ছে
                a[j] = temp;     // temp থেকে বড় ভ্যালুটা a[j]-এ বসানো হচ্ছে
            }
        }
        /*
        for (int i = 0; i < n; i++)
        {
            printf("%d ", a[i]);
        }
         1 9 3 7 5
         1 3 9 7 5
         1 3 5 9 7
         1 3 5 7 9
         1 3 5 7 9

        */
    }

    // ✅ শেষমেশ সাজানো অ্যারেটি প্রিন্ট করা হচ্ছে
    for (int i = 0; i < n; i++)
    {
        printf("%d ", a[i]); // প্রতিটি ভ্যালু স্পেস সহ প্রিন্ট
    }

    return 0; // প্রোগ্রাম সফলভাবে শেষ
}
