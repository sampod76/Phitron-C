#include <stdio.h>

int main()
{
    int x = 10; // ✅ integer ভ্যারিয়েবল x তৈরি করা হলো, মান 10

    printf("%d\n", x); // ✅ x এর মান প্রিন্ট করবে → 10

    printf("%p\n", &x); // ✅ &x = x ভ্যারিয়েবলের মেমরি অ্যাড্রেস (RAM এ কোথায় আছে)

    int *prt; // ✅ pointer ভ্যারিয়েবল prt ঘোষণা করা হলো
              // ✅ pointer এমন ভ্যারিয়েবল যা অন্য ভ্যারিয়েবলের অ্যাড্রেস ধরে রাখে

    prt = &x; // ✅ prt তে x এর মেমরি অ্যাড্রেস রাখা হলো

    printf("%p\n", prt); // ✅ prt = x এর অ্যাড্রেস, তাই x এর অ্যাড্রেসই প্রিন্ট হবে

    printf("%p\n", &prt); // ✅ &prt = prt pointer ভ্যারিয়েবল নিজে যে অ্যাড্রেসে আছে

    printf("%d\n", *prt); // ✅ *prt = pointer dereference
                          // ✅ মানে prt যে অ্যাড্রেস ধরে রেখেছে সেখানে যে value আছে তা প্রিন্ট করবে
                          // ✅ অর্থাৎ x এর মান → 10

    *prt = 200; // ✅ pointer ব্যবহার করে x এর মান পরিবর্তন করা হলো
                // ✅ prt → x এর অ্যাড্রেস ধরে, তাই *prt = 200 → x এর মান এখন 200

    printf("%d ", *prt); // ✅ এখন dereference করলে 200 দেখাবে

    return 0;
}
